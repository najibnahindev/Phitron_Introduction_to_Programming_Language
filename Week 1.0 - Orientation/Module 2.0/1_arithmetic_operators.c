#include<stdio.h>
int main()
{
    printf("%d\n", 6 + 3);  //addition
    printf("%d\n", 6 - 3);  //substruction
    printf("%d\n", 6 * 3);  //multiplication
    printf("%d\n", 6 / 3);  //division
    printf("%d\n", 6 % 5);  //modulus

    printf("%f\n", 8 / 3.0);  //division with typecasting 
    printf("%f\n", (float)8 / 3);  //division with typecasting 2.0
    return 0;
}